function getglobalPlasticProduction(year, year0) {
  let mass = 0;
  for (const [key, value] of Object.entries(sectorInfo)) {
    mass += plasticProduction(year0) * value[0] * value[1];
  }
  return mass;
}

function surfacePlastic(year, year0) {
  counter++;
  if (year > year0) {
    return (
      i * getglobalPlasticMass(year, year0) +
      r * (1 - d) * surfaceMass(year - 1, year0) +
      (1 - d) * coastalMass(year - 1, year0)
    );
  }
  return 0;
}

function coastalMass(y, y0) {
  return (1 - s) * (1 - t) * surfacePlastic(y, y0);
}

function surfaceMass(y, y0) {
  if (y > y0) {
    return (
      (1 - r) * (1 - d) * surfaceMass(y - 1, y0) + s * surfacePlastic(y, y0)
    );
  }
  return 0;
}

